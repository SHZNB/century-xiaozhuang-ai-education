export function buildAgentCatalog() {
  let index = 0;
  return categories.flatMap(([category, color, bg, icon, owner, items], categoryIndex) =>
    items.map(([name, logoText], itemIndex) => {
      index += 1;
      return {
        id: `agent-${String(index).padStart(3, "0")}`,
        name,
        logoText,
        category,
        icon,
        description: descriptions[category],
        color,
        bg,
        owner,
        status: index % 17 === 0 ? "draft" : index % 29 === 0 ? "offline" : "published",
        mode: "local",
        url: "",
        cozeUrl: `https://www.coze.cn/store/agent/xiaozhuang-century-${String(index).padStart(3, "0")}`,
        apiUrl: "",
        providerAlias: "",
        routeHint: routeForCategory(category),
        systemPrompt: `你是“${name}”，服务于南京晓庄学院百年晓庄智慧教育平台。你的领域是${category}。请结合晓庄“教学做合一”的教育精神，给出准确、可执行、适合真实校园场景的回答；涉及学校事实、政策、数据时不得编造。`,
        logoImage: `assets/agents/agent-${String(index).padStart(3, "0")}.svg`,
        featured: itemIndex === 0 && categoryIndex < 8,
        prompts: [
          `请介绍${name}可以提供哪些帮助`,
          `帮我完成一个与${category}相关的任务`,
          "给我一份可以马上执行的建议"
        ],
        createdAt: "2026-06-15",
        updatedAt: "2026-06-15"
      };
    })
  );
}

